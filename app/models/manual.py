class PageManual(Base):
    """페이지별 메뉴얼 — 슈퍼어드민이 수정, AI 자동 생성 확장 가능."""
    __tablename__ = "page_manuals"

    page_key   = Column(String(50), primary_key=True)   # products, campaigns …
    title      = Column(String(200), nullable=False, default="")
    description = Column(Text, nullable=True)           # 기능 설명
    how_to     = Column(Text, nullable=True)            # 사용 방법
    examples   = Column(Text, nullable=True)            # 실제 예시
    warnings   = Column(Text, nullable=True)            # 주의사항
    source     = Column(String(20), default="manual")   # "manual" | "ai"
    updated_at = Column(DateTime, default=datetime.utcnow, onupdate=datetime.utcnow)
